void main(){
    var angka = 0;
    print("Looping Pertama");
    while(angka < 20){
        angka += 2;
        print(angka.toString() + " - I Love Coding");
    }

    var angka2 = 20;
    print("Looping Kedua");
    while(angka2 > 2){
        angka2 -= 2;
        print(angka2.toString() + " - I Will Become A Mobile Developer");
    }
}