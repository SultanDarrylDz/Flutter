void main(){
    //equal operator
    var angka = 17;
    print(angka == 100);
    print(angka == 17);

    //not equal
    var sifat = "rajin";
    print(sifat != "malas");
    print(sifat != "bandel");

    //kurang lebih & lebih dari
    var number = 17;
    print( number < 20 ); // true
    print( number > 17 ); // false
    print( number >= 17 ); // true, karena terdapat sama dengan
    print( number <= 20 ); // true
}